"""Cold/warm timing benchmark at default config (768x1360, 4 steps).

Generates 3 images. The first includes pipeline init (cold). The remaining
two are averaged for "warm time per image". Writes a markdown table to
outputs/benchmark.md so the README can reference the file for numbers
without committing the raw artifact.

Run AFTER smoke_test passes.
"""

from __future__ import annotations

import platform
import statistics
import subprocess
import sys
import time
from pathlib import Path

import torch

from src.config import InferenceConfig
from src.pipeline import FluxGenerator

PROMPT = (
    "a detailed scientific illustration of a fruit fly brain, "
    "calcium imaging visualization, fluorescent neurons on dark background"
)


def _gpu_name() -> str:
    if torch.cuda.is_available():
        return torch.cuda.get_device_name(0)
    return "cpu"


def main() -> int:
    repo = Path(__file__).resolve().parent.parent
    smoke = subprocess.run([sys.executable, str(repo / "scripts" / "smoke_test.py")])
    if smoke.returncode != 0:
        print("benchmark: smoke_test failed, aborting.", file=sys.stderr)
        return smoke.returncode

    cfg = InferenceConfig()

    # Cold timing includes pipeline init - that's the number users feel on
    # the first run after a fresh clone.
    t0 = time.perf_counter()
    gen = FluxGenerator(cfg)
    cold_image, cold_metrics = gen.generate_with_metrics(PROMPT)
    cold_total = time.perf_counter() - t0
    cold_image.save(cfg.output_dir / "bench_cold.png")

    # Warm timings: pipeline already loaded, only the per-image cost matters.
    warm_metrics = []
    for i in range(2):
        image, m = gen.generate_with_metrics(PROMPT)
        image.save(cfg.output_dir / f"bench_warm_{i}.png")
        warm_metrics.append(m)

    warm_elapsed = statistics.mean(m["elapsed_s"] for m in warm_metrics)
    warm_peak = max(m["peak_vram_gb"] for m in warm_metrics)

    total_png_kb = (
        sum(
            (cfg.output_dir / name).stat().st_size
            for name in ("bench_cold.png", "bench_warm_0.png", "bench_warm_1.png")
        )
        / 1024
    )

    md = f"""# Benchmark — FLUX.1 [schnell] local inference

Generated by `scripts/benchmark.py`.

| metric                         | value |
| ------------------------------ | ----- |
| GPU                            | {_gpu_name()} |
| OS / kernel                    | {platform.system()} / {platform.release()} |
| Resolution                     | {cfg.width} x {cfg.height} |
| Inference steps                | {cfg.num_inference_steps} |
| Offload strategy               | {cfg.offload_strategy} |
| Cold-start total (load + 1 gen)| {cold_total:.1f} s |
| Cold gen only                  | {cold_metrics["elapsed_s"]:.1f} s |
| Warm gen (avg of 2)            | {warm_elapsed:.1f} s |
| Peak VRAM (cold)               | {cold_metrics["peak_vram_gb"]:.2f} GB |
| Peak VRAM (warm, max)          | {warm_peak:.2f} GB |
| Output size (3 PNGs total)     | {total_png_kb:.0f} KB |

Prompt used:
> {PROMPT}
"""
    bench_path = cfg.output_dir / "benchmark.md"
    bench_path.write_text(md)
    print(md)
    print(f"wrote {bench_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
